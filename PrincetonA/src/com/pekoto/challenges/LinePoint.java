package com.pekoto.challenges;

public class LinePoint {
    public double x;
    public double y;
    
    public LinePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public void setLocation(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
