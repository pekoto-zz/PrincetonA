package com.pekoto.challenges;

/*
 * A simple tuple class for verification purposes
 */
public class SimpleTuple {

    private int a;
    private int b;
    
    public SimpleTuple(int a, int b) {
        this.a = a;
        this.b = b;
    }
    
    public int getA() {
        return a;
    }
    
    public int getB() {
        return b;
    }
}
